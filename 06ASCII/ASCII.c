#include <stdio.h>
void main()
{
    char a = 'A';
    printf("The FullForm of ASCII is American Standard Code for information interchange.\n");

    printf("The ascii value of the 'A' char is %d.\n", a);
    //The ascii value of the 'A' char is 65.

    printf("The ascii value of the 'B' char is %d.\n", 'B');
    //The ascii value of the 'B' char is 66.

    printf("The ascii value of the 'C' char is %d.\n", 'C');
    //The ascii value of the 'C' char is 67.

    printf("The ascii value of the 'D' char is %d.\n", 'D');
    //The ascii value of the 'D' char is 68.

    printf("The ascii value of the 'a' char is %d.\n", 'a');
    // The ascii value of the 'a' char is 97.
}